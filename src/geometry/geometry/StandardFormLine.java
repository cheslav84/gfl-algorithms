package geometry.geometry;

public class StandardFormLine {
    private double a;
    private double b;
    private double c;

    public StandardFormLine(double a, double b, double c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }
    public double getC() {
        return c;
    }

}
